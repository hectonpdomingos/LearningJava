public class Metodo_Classe_Matematica {
    public static void main (String[] args){
        //Math.abs mostra valores absolutos
        System.out.println(Math.abs(-26.7));
        //Math.ceil arredonda os valores para cima
        System.out.println(Math.ceil(7.4));
        //Math.Floor  arredonda os valores para baixo
        System.out.println(Math.floor(7.4));
        //Math.max  exibe o valor maximo dentro das variais separadas por virgula
        System.out.println(Math.max(8.6,5.2));
        //Math.min exibe o valor minimo dentro das variaveis respadas por virgula
       System.out.println(Math.min(8.6,5.2)); 
       //Math.pow é usado para expressar potencia, exemplo: 8 elevado a 6,
       //separado por virgula
       System.out.println(Math.pow(2, 2));
      //Math.sqrt  usado para encontrar a raiz quadrada do numero em questão
       System.out.println(Math.sqrt(8));
      
       
    }
    
}
